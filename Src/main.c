static void changeLedConfig(uint8_t modeDecode)
{
	spiDeviceControl_t spiDevice;

	if(modeDecode == 1)
	{
		spiDevice.driverParams.testMode = 0x00;
		spiDevice.driverParams.scanMode = 0x07;
		spiDevice.driverParams.intensivity = 0x01;
		spiDevice.driverParams.decodeMode	= 0xff;
		spiDevice.driverParams.powerOn = 0x01;
	}
	else
	{
		spiDevice.driverParams.testMode = 0x00;
		spiDevice.driverParams.scanMode = 0x07;
		spiDevice.driverParams.intensivity = 0x01;
		spiDevice.driverParams.decodeMode	= 0x00;
		spiDevice.driverParams.powerOn = 0x01;
	}

	updateLedDriverConfig(spiDevice, 0);
}

