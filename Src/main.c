static void mainTaskLoop(void)
{
	uint8_t 	dispMode = 0;
	uint8_t 	data2Display[8];

	initLogger();
	initLedDriver();
	initJoystick();

	//initLcdScreen();

	initUltraSoundDriver();
	//initTransmitterAM433();
	initEeptom24CxDriver();

	while(1)
	{
		memset(data2Display, 0, sizeof(data2Display));

		if (0 == dispMode)
		{
			changeLedConfig(0);
			displayAsia();
		}

		if (1 == dispMode)
		{
			changeLedConfig(1);
			getJoystickPositionTextTrimmed(data2Display, data2Display+4);
			decodeNumber4Led(0, data2Display);
		}

		if (2 == dispMode)
		{
			changeLedConfig(1);
			getDistanceTextTrimmed(data2Display);
			decodeNumber4Led(0, data2Display);
		}

		{
			uint8_t		buff[40];
			readData(0x00, buff, 40);
			logMessage(LOG_INF, buff);
		}

		dispMode++;
		if (dispMode > 2)
		{
			dispMode = 0;
		}
		osDelay(1000);
	}
}


